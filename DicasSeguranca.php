                        <p>Durante o inverno é muito comum a incidência de neblina nas estradas. 
                        Os meses entre maio e julho apresentam os índices mais altos do fenômeno, 
                        sobretudo nas regiões sul e sudeste do Brasil. A neblina acontece por excesso de umidade no ar 
                        ou numa geada e geralmente em pontos de variação de temperatura, como na subida de serras, 
                        nos vales e perto de lagos e rios. Por isso, é importante que os motoristas estejam atentos e 
                        prevenidos para que essa situação adversa não seja a responsável por acidentes. 
                        É fundamental que os condutores chequem as condições climáticas antes de pegar a estrada. 
                        Além disso, devem verificar as condições do sistema de iluminação (luzes de direção, faróis, lanternas), 
                        das palhetas do limpador do para-brisa e do reservatório de água do lavador, que deve estar cheio. 
                        Confira algumas dicas importantes para a incidência de neblina, de acordo com o Observatório Nacional de Segurança Viária: 
                        </br></br>1. Fique atento tanto a veículos mais lentos à frente quanto a veículos que poderão vir atrás sem reduzir a velocidade. 
                        </br></br>2. Motoristas com pouca experiência têm a tendência a frear repentinamente. Nesses casos não use a buzina ou faróis para chamar a atenção. 
                        </br></br>3. A neblina molha a pista e caso haja óleo, pode formar uma emulsão que vai diminuir a aderência entre o pneu e o asfalto. 
                        </br></br>4. Qualquer freada ou movimento brusco da direção pode gerar falta de aderência dos pneus com a pista, e consequentemente a perda de controle do veículo. 
                        </br></br>5. Nunca use o pisca-alerta com o veículo em movimento. 
                        </br></br>6. Se ficar difícil de enxergar as lanternas do veículo à frente, é sinal que a neblina está ficando mais espessa. 
                        </br></br>7. Diminua a velocidade com cuidado para que o veículo que vem atrás faça o mesmo. 
                        </br></br>8. Caso haja necessidade de parada, faça em local fora de estrada, nunca no acostamento. Procure um abrigo ou estacionamento. 
